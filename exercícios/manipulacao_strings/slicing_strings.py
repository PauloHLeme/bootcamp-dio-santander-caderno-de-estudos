nome = "Paulo Henrique Leme"

print(nome[4]) # Pega apenas o elemento na posição desejada

print(nome[:5]) # Pega do primeiro ao quinto elemento

print(nome[15:]) # Pega do décimo quinto ao último elemento

print(nome[3:16:2]) # Pega do terceiro ao décimo sexto elemento, mas pulando de 2 em 2

print(nome[-2]) # Pega o penúltimo elemento

print(nome[::-1]) # Espelha o nome
